public class Arrayexample2 {
    public static void main(String[] args) {
        // array = collection of same data type items
        // initialises array and puts all data inside
        String [] fNames = {"Harry", "Ron", "Beverly", "Marianela"};

        // prints each item using index number
        System.out.println(fNames[0]); //outputs Harry
        System.out.println(fNames[1]); //outputs Ron
        System.out.println(fNames[2]); //outputs Beverly

        // find the length pr how many items are inside array
        System.out.println("The array lenght is : " + fNames.length);


    }
}
